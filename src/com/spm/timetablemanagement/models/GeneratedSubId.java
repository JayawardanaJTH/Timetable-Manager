package com.spm.timetablemanagement.models;

/**
 *
 * @author hvdil
 */
public class GeneratedSubId {
    
    private int id;
    private String sGid;
    
    public GeneratedSubId(int id, String sGid){
        this.id = id;
        this.sGid = sGid;
    } 
    
    public int getId()
    {
        return id;
    }
    
    public String getSgId()
    {
        return sGid;
    }
}
